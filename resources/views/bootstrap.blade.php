O que é o Bootstrap e para que serve?

Os frameworks oferecem uma estrutura de projetos web baseada em colunas, algo que economiza tempo e facilita a criação de projetos para todos os tipos de dispositivos. Um dos frameworks HTML, CSS e JS mais utilizados é o Bootstrap. Graças à sua eficiência no desenvolvimento de interfaces gráficas, 26.6% dos sites do mundo usam este framework nas suas bibliotecas.

O Bootstrap foi criado pelo Twitter em 2011. É um framework de código aberto e gratuito, utilizado por desenvolvedores Front-end e Full Stack para criar designs de sites e aplicativos. 

O Bootstrap tem como objetivo a construção de sites responsivos. O design responsivo garante que todos os elementos da interface funcionem seguindo o conceito mobile first, ou seja, que o design web inicialmente criado pensando em tablets e smartphones se adapte a outros dispositivos, como desktops.

O Bootstrap se baseia em um sistema de colunas ou grades responsivas, que ajudam a distribuir o conteúdo em linhas e colunas, facilitando a inserção de dados. Uma grade tem 12 colunas que se combinam de diversas maneiras.

Os containers no Bootstrap são blocos de construção que contêm e alinham o conteúdo na tela. Existem três tipos de containers de dados: um container fixo (.container) de largura fixa, .container-{breakpoint}, em 100% até um breakpoint determinado e o .container-fluid, capaz de se ajustar à resolução da tela.

Grid System:

.container: Define um container responsivo.
.row: Define uma linha no grid.
.col: Define uma coluna no grid.

Tipografia:

.display-*: Define estilos de exibição para diferentes tamanhos.
.h1 a .h6: Define estilos para cabeçalhos.
.lead: Estiliza um parágrafo para se destacar.

Cores:

.text-*: Define a cor do texto.
.bg-*: Define a cor de fundo.

Espaçamento e Margens:

.m-*: Define margens.
.p-*: Define preenchimentos.

Bordas:

.rounded-*: Adiciona bordas arredondadas.
.border: Adiciona uma borda.

Componentes de Navegação:

.navbar: Define uma barra de navegação.
.nav: Define uma lista de navegação.

Botões:

.btn: Define um botão.
.btn-*: Define estilos específicos para botões.

Formulários:

.form: Define estilos para formulários.
.form-control: Estiliza controles de formulário.

Cards:

.card: Define um bloco de cartão.
.card-header, .card-body, .card-footer: Define partes específicas de um cartão.

Modal:

.modal: Define um contêiner modal.