    <section class="slide">
        <h2>Tags Semânticas - Estrutura</h2>
        <p style="font-size: 0.75rem; margin-top: -1.5rem;">
            São as tags fundamentais que ajudarão a estruturar um documento HTML5 de maneira semântica, fornecendo significado claro e facilitando a compreensão do conteúdo tanto por humanos quanto por máquinas (Google, IA's).
        </p>
        <div class="columns two">
            <dl style="font-size: 0.85rem; margin-top: -0.5rem;">
                <dt> &lt;!DOCTYPE html&gt;: </dt>
                <dd> Define a versão do HTML que está sendo usada (HTML5).</dd>

                <dt>&lt;html&gt; &lt;/html&gt;:</dt>
                <dd>Define o início e o fim do documento HTML.</dd>

                <dt>&lt;head&gt;:</dt>
                <dd>Contém informações sobre o documento, como metadados, título e referências a estilos e scripts.</dd>

                <dt>&lt;title&gt;:</dt>
                <dd>Define o título da página, exibido na barra de título do navegador.</dd>
                
            </dl>

            <dl style="font-size: 0.85rem; margin-top: -0.5rem;">

                <dt>&lt;body&gt; &lt;/body&gt;:</dt>
                <dd>Contém o conteúdo principal da página.</dd>

                <dt> &lt;header&gt; &lt;/header&gt;: </dt>
                <dd> Representa a introdução ou um cabeçalho da seção (logotipos, títulos, navegação, etc).</dd>

                <dt>&lt;nav&gt; &lt;/nav&gt;:</dt>
                <dd>Agrupa os links da navegação principal do site. </dd>

                <dt>&lt;footer&gt; &lt;/footer&gt;: </dt>
                <dd>Define o rodapé ou área de conclusão de um documento ou de uma seção.
                </dd>
                
            </dl>
            
        </div>   
    </section>

    <section class="slide">
        <h2>Tags Semânticas - Conteúdos</h2>

        <div class="columns two">
            <dl style="font-size: 0.73rem; margin-top: -0.5rem;">
                <dt>&lt;main&gt;: </dt>
                <dd> Define o conteúdo principal da página, excluindo cabeçalhos, rodapés e barras laterais.</dd>

                <dt>&lt;article&gt;:</dt>
                <dd>Representa um conteúdo independente e autossuficiente que pode ser reutilizado e distribuído separadamente, como um post de blog.</dd>

                <dt>&lt;section&gt;:</dt>
                <dd>Agrupa conteúdo tematicamente relacionado dentro de um documento. Pode conter &lt;header&gt; e &lt;footer&gt;.</dd>

                <dt>&lt;aside&gt;:</dt>
                <dd>Representa conteúdo relacionado ao conteúdo ao redor, como barras laterais ou notas laterais.</dd>

                <dt>&lt;h1&gt; a &lt;h6&gt;:</dt>
                <dd>Define títulos de diferentes níveis de importância esua hierarquia. &lt;h1&gt; é o mais importante, e &lt;h6&gt; é o menos importante.</dd>
                
            </dl>

            <dl style="font-size: 0.73rem; margin-top: -0.5rem;">

                <dt> &lt;p&gt;: </dt>
                <dd> Define um parágrafo. Deum parágrafo para o outro mudamos o foco do mesmo assunto.</dd>

                <dt>&lt;a&gt;:</dt>
                <dd>Cria um hyperlink para navegação ou referência a outros recursos/páginas.</dd>

                <dt>&lt;img&gt;:</dt>
                <dd>Insere uma imagem na página.
                </dd>

                <dt>&lt;ul&gt; , &lt;ol&gt; e &lt;dl&gt;:</dt>
                <dd>Criam listas não ordenadas, ordenadas e de descrição, respectivamente.</dd>

                <dt>&lt;li&gt; , &lt;dt&gt; e &lt;dd&gt;:</dt>
                <dd>Criam itens de listas, item e descrição do item de lista, respectivamente.</dd>

                <dt>&lt;strong&gt;,  &lt;em&gt; e &lt;br&gt;:</dt>
                <dd>Enfatiza o texto com negrito, Enfatiza o texto com Italico e quebra de linha.</dd>
                
            </dl>
            
        </div>   
    </section>

